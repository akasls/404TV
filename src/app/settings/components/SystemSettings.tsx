const DraggableChannelList = ({
  channelOrder,
  setChannelOrder,
  customFilters,
}: any) => {
  const [draggedIdx, setDraggedIdx] = useState<number | null>(null);

  const available = useMemo(() => {
    const customLabels = customFilters.map((f: any) => f.name);
    const defaultsTemplate = [
      { id: 'movie', label: '电影' },
      { id: 'tv', label: '电视剧' },
      { id: 'minitv', label: '短剧' },
      { id: 'anime', label: '动漫' },
      { id: 'show', label: '综艺' },
    ];
    const defaults = defaultsTemplate.filter(
      (d) => !customLabels.includes(d.label)
    );
    const customs = customFilters.map((f: any) => ({
      id: f.name,
      label: f.name,
    }));
    return [...defaults, ...customs];
  }, [customFilters]);

  // Sync missing items
  const items = useMemo(() => {
    const list = [...(channelOrder || [])];
    if (list.length === 0) {
      list.push('movie', 'tv', 'minitv', 'anime', 'show'); // default fallback
    }
    available.forEach((a) => {
      if (!list.includes(a.id)) list.push(a.id);
    });
    return list.filter((id) => available.some((a) => a.id === id));
  }, [channelOrder, available]);

  const onDragStart = (e: React.DragEvent, idx: number) => {
    setDraggedIdx(idx);
    e.dataTransfer.effectAllowed = 'move';
  };

  const onDragOver = (e: React.DragEvent, idx: number) => {
    e.preventDefault();
    if (draggedIdx === null || draggedIdx === idx) return;

    const newList = [...items];
    const [removed] = newList.splice(draggedIdx, 1);
    newList.splice(idx, 0, removed);

    setChannelOrder(newList);
    setDraggedIdx(idx);
  };

  const onDragEnd = () => {
    setDraggedIdx(null);
  };

  return (
    <div className='flex flex-wrap gap-2 w-full p-3 border border-gray-300 dark:border-gray-600 rounded-lg bg-white dark:bg-gray-800 shadow-inner min-h-[50px]'>
      {items.map((id, idx) => {
        const info = available.find((a) => a.id === id);
        if (!info) return null;
        return (
          <div
            key={id}
            draggable
            onDragStart={(e) => onDragStart(e, idx)}
            onDragOver={(e) => onDragOver(e, idx)}
            onDragEnd={onDragEnd}
            className={`cursor-move px-4 py-2 bg-gradient-to-br from-green-50 to-green-100 dark:from-green-900/40 dark:to-green-800/20 border border-green-200 dark:border-green-700/50 rounded-lg shadow-sm text-sm font-semibold text-green-800 dark:text-green-100 hover:shadow-md hover:-translate-y-0.5 transition-all duration-200 ${draggedIdx === idx ? 'opacity-30 scale-95' : 'scale-100'
              }`}
          >
            {info.label}
          </div>
        );
      })}
    </div>
  );
};

export default function SystemSettings() {
  return (
    <Suspense>
      <AdminPageClient />
    </Suspense>
  );
}
